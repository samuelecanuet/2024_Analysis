// Open ROOT File
TFile* MyTFile(string filename, string option, string myoption="", int indent = 0)
{
    TFile *file = new TFile(filename.c_str(), option.c_str());
    filename = filename.substr(filename.find_last_of("/\\") + 1);
    if (file->IsZombie())
    {
        Warning("Not Found: " + filename);
        return NULL;
    }
    else if (option == "READ")
    {
        if (myoption != "Q")
        {
            Success("Read: " + filename, indent);
        }
    }
    else if (option == "RECREATE")
    {
        if (myoption != "Q")
        {
        Success("Recreated: " + filename, indent);
        }
    }
    else if (option == "UPDATE")
    {
        if (myoption != "Q")
        {
        Success("Updated: " + filename, indent);
        }
    }
    else if (option == "NEW")
    {
        if (myoption != "Q")
        {
        Success("Created: " + filename, indent);
        }
    }
    else
    {
        Error("Unknown option: " + filename);
        return NULL;
    }
    return file;
}

// Open ROOT Tree
TTree* MyTTree(TFile* file, string tree_name)
{
    TTree *tree = (TTree *)file->Get(tree_name.c_str());
    if (tree == NULL)
    {
        Error("Not Found: " + tree_name + " in " + file->GetName());   
    }
    return tree;
}

void WriteTime(TFile* from_File, TFile* to_file)
{
  from_File->cd();
  TObject* start = from_File->Get("Start_Time");
  TObject* stop = from_File->Get("End_Time");

  if (start == NULL || stop == NULL)
  {
    Error("No Start or Stop time found");
  }

  to_file->cd();
  TNamed("Start_Time", start->GetTitle()).Write();
  TNamed("End_Time", stop->GetTitle()).Write();
}

pair<string, string> GetTime(TFile* File)
{
  TObject* start = File->Get("Start_Time");
  TObject* stop = File->Get("End_Time");

  string str_start = start->GetTitle();
  string str_stop = stop->GetTitle(); 

  return make_pair(str_start, str_stop);
}

string SearchFiles(const string &directory, const string &run_number, string OPTION="")
{
  DIR *dir;
  struct dirent *ent;
  if ((dir = opendir(directory.c_str())) != NULL)
  {
    while ((ent = readdir(dir)) != NULL)
    {
      string filename = ent->d_name;
      if (filename.find(("run_" + run_number).c_str()) == 0)
      {
		if (!OPTION.find("Q"))
        	Info(("Found file: " + filename).c_str());
        closedir(dir);
        return filename;
      }
    }
	if (!OPTION.find("W"))
		Error(("No file found for run: " + run_number).c_str());
    Warning(("No file found for run: " + run_number).c_str());
    closedir(dir);
    return "";
  }
  else
  {
    Error(("Could not open directory: " + directory).c_str());
    return "";
  }
}

int ConvertBase5ToBase10(int base5_int)
{
	string base5 = to_string(base5_int);
	int num = 0;
	for (char digit : base5)
	{
		num = num * 5 + (digit - '0');
	}
	return num;
}
double computeMedian(std::vector<double> &values)
{
	if (values.empty())
		Error("Cannot compute median of an empty list");
	

	std::sort(values.begin(), values.end());
	size_t size = values.size();
	size_t mid = size / 2;

	if (size % 2 == 0)
	{
		return (values[mid - 1] + values[mid]) / 2.0;
	}
	else
	{
		return values[mid];
	}
}

double Convert_DatetoTimeSec(string datestring, bool FLAG2021=false)
{
	tm date = {}, referenceDate = {};
	istringstream dateStream(datestring);

	if (!FLAG2021)
	{
		dateStream >> get_time(&date, "%Y-%m-%d at %Hh-%Mm-%Ss");
		date.tm_year += 2000;
	}
	else
	{
		dateStream >> get_time(&date, "%d-%m-%Y %H:%M:%S");
	}

	time_t timeInSeconds = mktime(&date);
	return timeInSeconds;
}

double Diff_Date(string datestring, string refstring, bool FLAG2021=false)
{
	return Convert_DatetoTimeSec(datestring, FLAG2021) - Convert_DatetoTimeSec(refstring, FLAG2021);
}

int Freedman_Diaconis(TH1 *hist)
{
	int nEntries = hist->GetEntries();
	int nBins = hist->GetNbinsX();
	double xmin = hist->GetXaxis()->GetXmin();
	double xmax = hist->GetXaxis()->GetXmax();	
	double q1, q3;
	hist->GetQuantiles(1, &q1, new double[1]{0.25}); 
	hist->GetQuantiles(1, &q3, new double[1]{0.75}); 

	double IQR = q3 - q1; 
	double binWidth = 2 * IQR / pow(nEntries, 1.0 / 3.0);
	int nBins_corrected = (xmax - xmin) / binWidth;

	int Rebin_factor = nBins / nBins_corrected;

	while (nBins % Rebin_factor != 0 && Rebin_factor > 1)
	{
		Rebin_factor--;
	}

	return Rebin_factor;
}

